const { Schema, model } = require("mongoose");

// Create Schema
const ProductSchema = new Schema({
    name: {
        type: String,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    category: {
        type: String,
        required: true,
    },
    image: {
        type: String,
        required: true,
    },
});

const ProductModel = model("products", ProductSchema);

module.exports = { ProductModel }